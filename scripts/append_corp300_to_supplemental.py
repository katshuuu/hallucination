def main() -> None:
    root = Path(__file__).resolve().parents[1]
    path = root / "data" / "supplemental" / "labeled_qa_diverse_v1.csv"
    triplets = _triplets()

    existing: list[list[str]] = []
    seen: set[tuple[str, str]] = set()
    with path.open(encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        assert fieldnames == ["id", "prompt", "response", "label", "source"]
        for row in reader:
            p, r = row["prompt"].strip(), row["response"].strip()
            seen.add((p, r))
            existing.append([row["id"], row["prompt"], row["response"], row["label"], row["source"]])

    ids = {row[0] for row in existing}
    if "sup201" in ids:
        print(f"append_corp300: batch already merged (sup201 present), skip — {path}")
        return

    start = 201
    new_rows: list[list[str]] = []
    for i, (q, good, bad) in enumerate(triplets):
        for j, (text, lab) in enumerate(((good, 0), (bad, 1))):
            key = (q.strip(), text.strip())
            if key in seen:
                raise SystemExit(f"duplicate pair: {key!r}")
            seen.add(key)
            rid = f"sup{start + i * 2 + j:03d}"
            new_rows.append([rid, q, text, str(lab), "supplemental"])

    assert len(new_rows) == 300

    with path.open("w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerow(["id", "prompt", "response", "label", "source"])
        for row in existing:
            w.writerow(row)
        for row in new_rows:
            w.writerow(row)

    print(f"Appended {len(new_rows)} rows to {path} (total {len(existing) + len(new_rows)})")


if __name__ == "__main__":
    main()
